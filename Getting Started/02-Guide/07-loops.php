<?php

# For Loop

echo "For Loop";
echo "<br/>";
for ($i = 0; $i < 10; $i++) {
    echo "Truth";
    echo "<br/>";
}

echo "i is " . $i;
echo "<br/>";

# While Loop
echo "While Loop";
echo "<br/>";
$j = 0;
while ($j < 10) {
    echo "Truth";
    echo "<br/>";
    $j++;
}

echo "j is " . $j;
echo "<br/>";

# Do-While Loop
echo "Do-While Loop";
echo "<br/>";

$k = 0;
do {
    echo "Truth";
    echo "<br/>";
    $k++;
} while ($k < 10);

echo "k is " . $k;
echo "<br/>";
